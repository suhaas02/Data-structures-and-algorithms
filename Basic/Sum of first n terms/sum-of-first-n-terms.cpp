class Solution {
  public:
    long long sumOfSeries(long long N) {
        // code here
        if(N == 1 or N == 2)
            return N;
        long long int sum = 1;
        for(int i = 2; i <= N; i++)
        {
            sum += powl(i, 3);
        }
        return sum;
    }
};

